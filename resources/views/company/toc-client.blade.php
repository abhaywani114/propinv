<strong>LEGIONELLA RISK ASSESSMENTS</strong>
<br/>
<br/>
PRMS provides a Legionella Risk Assessment Service to comply with the landlord’s obligations
under the Health & Safety at Work etc Act 1974. This service is to assess and report on whether
there is an increased risk of Legionella being present in the water system and if a risk is identified
to provide appropriate recommendations for either the landlord or tenant to reduce or eliminate
the risk.
<br/>
<br/>
The Risk Assessment is conducted in accordance with ACOP (Approved Code of Practice) L8 to
include:
<br/>
<br/>
i. The recording of the water temperature of all hot and cold water outlets.
<br/>
<br/>
ii. The recording of the temperature of the hot water tank (calorifier) outlet pipework.
<br/>
<br/>
iii. A visual inspection of all tap spouts and shower/spray heads.
<br/>
<br/>
iv. A visual inspection of the hot water tank and associated plumbing where applicable.
<br/>
<br/>
v. Advising on any redundant pipework (dead legs) and/or flexible hoses visible.
<br/>
<br/>
vi. A visual inspection of the cold water tank where applicable.
<br/>
<br/>
vii. Advising on exterior features including water features, fountains, hot tubs and exterior
taps where applicable.
<br/>
<br/>
The Risk Assessment does not constitute a Legionella ‘test’ and no samples of water will be taken
for analysis. The client will be advised of appropriate measures to be taken where necessary and
in the event of a Level 4 risk (most severe) being identified, PRMS will recommend that the
property be closed down immediately and that an expert contractor be appointed to
decontaminate the property.
<br/>
<br/>
The Assessor reserves the right not to access any areas of the property which are deemed unsafe
including loft spaces where there is either no or inadequate boarding allowing safe access to
inspect the cold water tank and/or boiler.
<br/>
<br/>
The Risk Assessment is limited to the condition and state of the water system as presented at the
time of the visit and PRMS cannot accept any liability for any increased Legionella Risk that may
arise at a subsequent date due to a change in circumstances, for instance the property being left
empty for an extended period of time or a defect occurring within the water heating system.
<br/>
<br/>
PRMS cannot accept any responsibility for subsequent loss or injury where any recommendations
are not followed up either before the property is let out or during the tenancy.
